import { Link, Link as RouterLink } from "@tanstack/react-router";
import { ChevronsUpDown, Home, LogOut, Settings } from "lucide-react";

import type { UserPublic } from "@/client";
import { Avatar, AvatarFallback } from "@/components/ui/avatar";
import {
  DropdownMenu,
  DropdownMenuContent,
  DropdownMenuItem,
  DropdownMenuLabel,
  DropdownMenuSeparator,
  DropdownMenuTrigger,
} from "@/components/ui/dropdown-menu";
import {
  SidebarMenu,
  SidebarMenuButton,
  SidebarMenuItem,
  useSidebar,
} from "@/components/ui/sidebar";
import useAuth from "@/hooks/useAuth";
import { getInitials } from "@/utils";

interface UserInfoProps {
  fullName?: string;
  email?: string;
}

function UserInfo({ fullName, email }: UserInfoProps) {
  return (
    <div className="flex items-center gap-2.5 w-full min-w-0">
      <Avatar className="size-8">
        <AvatarFallback className="bg-muted text-muted-foreground border border-sidebar">
          {getInitials(fullName || "User")}
        </AvatarFallback>
      </Avatar>
      <div className="flex flex-col items-start min-w-0">
        <p className="text-sm font-medium truncate w-full">{fullName}</p>
        <p className="text-xs truncate w-full">{email}</p>
      </div>
    </div>
  );
}

function getFullName(user: UserPublic): string {
  return `${user.first_name} ${user.last_name}`.trim();
}

export function User({ user }: { user: UserPublic | null | undefined }) {
  const { logout } = useAuth();
  const { isMobile, setOpenMobile } = useSidebar();

  if (!user) return null;

  const fullName = getFullName(user);

  const handleMenuClick = () => {
    if (isMobile) {
      setOpenMobile(false);
    }
  };
  const handleLogout = async () => {
    logout();
  };

  return (
    <SidebarMenu>
      <SidebarMenuItem>
        <DropdownMenu>
          <DropdownMenuTrigger asChild>
            <SidebarMenuButton
              size="lg"
              className="data-[state=open]:bg-sidebar-accent data-[state=open]:text-sidebar-accent-foreground"
              data-testid="user-menu"
            >
              <UserInfo fullName={fullName} email={user.email} />
              <ChevronsUpDown className="ml-auto size-4 text-sidebar-foreground" />
            </SidebarMenuButton>
          </DropdownMenuTrigger>
          <DropdownMenuContent
            className="w-(--radix-dropdown-menu-trigger-width) min-w-56 rounded-lg"
            side={isMobile ? "bottom" : "right"}
            align="end"
            sideOffset={4}
          >
            <DropdownMenuLabel className="p-0 font-normal">
              <UserInfo fullName={fullName} email={user.email} />
            </DropdownMenuLabel>
            <DropdownMenuSeparator />
            <RouterLink to="/dashboard/settings" onClick={handleMenuClick}>
              <DropdownMenuItem>
                <Settings />
                User Settings
              </DropdownMenuItem>
            </RouterLink>
            <DropdownMenuItem onClick={handleLogout}>
              <LogOut />
              Log Out
            </DropdownMenuItem>
            <Link to="/">
              <DropdownMenuItem>
                <Home />
                Go to main page
              </DropdownMenuItem>
            </Link>
          </DropdownMenuContent>
        </DropdownMenu>
      </SidebarMenuItem>
    </SidebarMenu>
  );
}
